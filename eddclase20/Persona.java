
package eddclase20;

public class Persona {
    String tipo;
    int edad;

    public Persona(String tipo, int edad) {
        this.tipo = tipo;
        this.edad = edad;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
}
